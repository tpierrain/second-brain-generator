// Rewrites the "local-mirror" server command in a .mcp.json object so that it
// goes through the OS-appropriate self-heal launcher. Preserves cwd/env/type. Mutates
// and returns the object. `platform` = value of process.platform. No-op (no throw) if
// the server is absent — local-mirror is opt-in, only wired once a source exists.
export function applyLocalMirrorLauncher(mcp, platform) {
  const srv = mcp?.mcpServers?.["local-mirror"];
  if (!srv) return mcp;
  if (platform === "win32") {
    srv.command = "cmd";
    srv.args = ["/c", "local-mirror\\launch.cmd"];
  } else {
    srv.command = "/bin/sh";
    srv.args = ["local-mirror/launch.sh"];
  }
  return mcp;
}

// Name of the transient win32 install script materialised inside rag/. Space-free
// and invoked by RELATIVE name (with cwd=rag/) so a brain path containing spaces
// (C:\Users\John Doe\…) can't break cmd's argument splitting.
const WIN_INSTALL_SCRIPT = "sbg-rag-install.cmd";

// Default fs seam for buildRagInstallInvocation: writes the win32 install script
// into rag/ and returns its (relative) base name; removeScript deletes it.
// Injected in tests so the seam stays pure (no implicit I/O).
const realInstallIo = {
  writeScript(ragDir, content) {
    writeFileSync(join(ragDir, WIN_INSTALL_SCRIPT), content);
    return WIN_INSTALL_SCRIPT;
  },
  removeScript(ragDir, name) {
    rmSync(join(ragDir, name), { force: true });
  },
};

// Builds the invocation that installs the rag/ deps THROUGH the same self-heal
// PATH the launcher uses at runtime (pathPrependSh/Cmd) — so the native binary
// (better-sqlite3) is moulded for exactly the Node that will later load it,
// killing the install-node ≠ runtime-node ABI skew (ADR 0021-A). Returns
// {command, args, cleanup} for child_process; run it with cwd = ragDir, then call
// cleanup() (a no-op on POSIX). On win32 the self-heal block + `npm install` are
// written to a real .cmd inside rag/ and invoked by a space-free, cwd-qualified
// relative name (`.\sbg-rag-install.cmd`, NOT a bare name — a hardened box with
// NoDefaultCurrentDirectoryInExePath set won't search cwd for a bare name; NOT
// passed as a multi-line `cmd /c` argument either, which cmd silently truncates
// after the first newline — the install was skipped yet exited 0; and an absolute
// temp path could carry spaces). `io` is injected for testability; reuses
// pathPrependCmd() so the self-heal block stays the single source of truth.
export function buildRagInstallInvocation(platform, ragDir, io = realInstallIo) {
  if (platform === "win32") {
    const script = `@echo off\r\n${pathPrependCmd()}\r\nnpm install --silent\r\n`;
    const name = io.writeScript(ragDir, script);
    // Qualify with `.\` (not a bare name): on a hardened box where
    // NoDefaultCurrentDirectoryInExePath is set, cmd does NOT search cwd for a bare
    // name → the install would fail. An explicit relative path is always resolved
    // against cwd (=rag/). Still space-free, so `John Doe` paths stay safe (B1).
    return { command: "cmd", args: ["/c", `.\\${name}`], cleanup: () => io.removeScript(ragDir, name) };
  }
  return {
    command: "/bin/sh",
    args: ["-c", `${pathPrependSh()}\nexec npm install --silent`],
    cleanup: () => {},
  };
}

// Returns a COPY of baseEnv where only PATH is neutralized (the rest — HOME,
// ProgramFiles, APPDATA, LOCALAPPDATA, NVM_SYMLINK… — is preserved because the
// self-heal needs it). Used by the install smoke-test: prove that the wrapper
// finds node ALL BY ITSELF, in the impoverished PATH like the desktop app (else
// the test inherits the rich PATH of the install shell → near-false-positive).
export function minimalPathEnv(platform, baseEnv) {
  if (platform === "win32") {
    // cmd.exe must stay findable (the smoke-test invokes it) → we keep just
    // System32; node will come from run-node.cmd's self-heal.
    const systemRoot = baseEnv.SystemRoot || "C:\\Windows";
    return { ...baseEnv, PATH: `${systemRoot}\\System32` };
  }
  // posix: sh is launched by absolute path (/bin/sh) → empty PATH, node will come
  // ONLY from the self-heal → proof that the wrapper is self-sufficient.
  return { ...baseEnv, PATH: "" };
}

// Builds the {{NODE}} replacement value for the hook commands in
// .claude/settings.json (statusLine, PostToolUse, SessionStart). The result is
// inserted as-is into a JSON string → the quotes are already escaped (\").
// Points by ABSOLUTE path to the OS-appropriate run-node.* self-heal launcher (the
// hook's cwd is not guaranteed on the desktop app side). We bake the resolved path
// (no nested {{PROJECT_ROOT}}) → no dependency on the order of substitutions.
// projectRootPosix = brain path normalized to "/" slashes.
export function nodeHookCommand(platform, projectRootPosix) {
  if (platform === "win32") {
    // In JSON, each Windows path separator must be an escaped backslash (\\).
    // split("/").join("\\\\") literally produces "\\" in the JSON text.
    const win = projectRootPosix.split("/").join("\\\\");
    return `cmd /c \\"${win}\\\\scripts\\\\run-node.cmd\\"`;
  }
  return `/bin/sh \\"${projectRootPosix}/scripts/run-node.sh\\"`;
}

